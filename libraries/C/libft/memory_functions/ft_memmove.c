#include "libft.h"

void	*ft_memmove(void *dst, void *src, size_t n)
{
	unsigned char	*tmp_dst;
	unsigned char	*tmp_src;

	tmp_dst = (unsigned char *)dst;
	tmp_src = (unsigned char *)src;
	if (dst <= src)
		return (ft_memcpy(dst, src, n));
	while (n--)
		tmp_dst[n] = tmp_src[n];
	return (dst);
}
