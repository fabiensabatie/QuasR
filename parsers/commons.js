/** Defining environment variables */
let __WARNING = 'warning';
let __NONBLOCKING = 'blocking';
let __FATAL = 'fatal';

let __ERR = function(string, level = __WARNING) {
	if (level == __WARNING) console.warn(string);
	else if (level == __NONBLOCKING) console.error(string);
	else if (level == __FATAL) throw Error(string);
	return (false);
}

export { __WARNING, __NONBLOCKING, __FATAL, __ERR }
